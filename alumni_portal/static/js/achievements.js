function ach_show_hide()
{
	if (document.getElementById("ach_show_hide").style.display=="none")
	{
		document.getElementById("ach_show_hide").style.display="inline";
		document.getElementById("ach_show_hide_btn").innerHTML = '-';
	}
	else
	{
		document.getElementById("ach_show_hide").style.display="none";
		document.getElementById("ach_show_hide_btn").innerHTML = '+';
	}
}
